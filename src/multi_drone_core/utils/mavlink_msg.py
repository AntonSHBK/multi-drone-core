from typing import Dict
from pymavlink import mavutil


def get_all_mavlink_messages() -> Dict[int, str]:
    """
    Возвращает словарь всех доступных MAVLink сообщений.

    Returns
    -------
    Dict[int, str]
        Словарь вида:
        {
            message_id: message_name
        }

    Основные сообщения и их назначение
    ----------------------------------

    HEARTBEAT (0)
        Базовое сообщение. Показывает, что автопилот активен.
        Содержит режим, статус системы и тип аппарата.

    SYS_STATUS (1)
        Состояние системы: батарея, загрузка CPU, статус сенсоров.

    SYSTEM_TIME (2)
        Системное время автопилота и синхронизация.

    PING (4)
        Проверка соединения и задержки (latency).

    SET_MODE (11)
        Установка режима полёта.

    PARAM_* (20-23)
        Работа с параметрами автопилота:
        - чтение
        - список
        - установка

    GPS_RAW_INT (24)
        Сырые GPS-данные.

    ATTITUDE (30)
        Ориентация: roll, pitch, yaw.

    LOCAL_POSITION_NED (32)
        Локальная позиция и скорость (x, y, z, vx, vy, vz).

    GLOBAL_POSITION_INT (33)
        Глобальная позиция (lat, lon, alt).

    RC_CHANNELS (65)
        Значения каналов управления (пульт).

    SERVO_OUTPUT_RAW (36)
        Выходы на моторы/сервоприводы.

    VFR_HUD (74)
        Высота, скорость, курс — агрегированное состояние.

    COMMAND_LONG (76)
        Универсальная команда управления (основной механизм команд).

    COMMAND_ACK (77)
        Подтверждение выполнения команды.

    SET_POSITION_TARGET_* (84, 86)
        Установка целевой позиции (основа offboard управления).

    POSITION_TARGET_* (85, 87)
        Текущая целевая позиция.

    ATTITUDE_TARGET (83)
        Целевая ориентация.

    BATTERY_STATUS (147)
        Детальная информация о батарее.

    AUTOPILOT_VERSION (148)
        Версия автопилота и capabilities.

    STATUSTEXT (253)
        Текстовые сообщения и ошибки.

    DEBUG / DEBUG_VECT (254, 250)
        Отладочная информация.

    SENSOR и IMU сообщения
    ----------------------
    RAW_IMU (27)
        Сырые данные IMU.

    SCALED_IMU (26)
        Отмасштабированные данные IMU.

    HIGHRES_IMU (105)
        Высокоточные данные IMU.

    SCALED_PRESSURE (29, 137, 143)
        Давление (барометр).

    DISTANCE_SENSOR (132)
        Датчики расстояния (лидар/ультразвук).

    RANGEFINDER (173)
        Дальномер.

    Навигация и оценка состояния
    ----------------------------
    EKF_STATUS_REPORT (193)
        Статус фильтра Калмана.

    ESTIMATOR_STATUS (230)
        Состояние оценивания (позиция, скорость).

    LOCAL_POSITION_NED_COV (64)
        Локальная позиция с ковариацией.

    GLOBAL_POSITION_INT_COV (63)
        Глобальная позиция с ковариацией.

    OPTICAL_FLOW (100)
        Данные оптического потока.

    VISION_POSITION_ESTIMATE (102)
        Позиция от внешней системы (SLAM / CV).

    Управление миссиями
    -------------------
    MISSION_* (37-47, 73)
        Работа с миссиями:
        - загрузка
        - запрос
        - выполнение
        - подтверждение

    FENCE_* (160-162)
        Геозоны.

    RALLY_* (175-176)
        Точки аварийной посадки.

    Камеры и полезная нагрузка
    --------------------------
    CAMERA_TRIGGER (112)
        Срабатывание камеры.

    CAMERA_STATUS (179)
        Статус камеры.

    CAMERA_FEEDBACK (180)
        Обратная связь от камеры.

    DIGICAM_* (154-155)
        Управление цифровой камерой.

    GOPRO_* (215-219)
        Управление GoPro.

    GIMBAL_* (200+)
        Управление подвесом.

    Логи и отладка
    --------------
    LOG_* (117-122)
        Работа с логами.

    REMOTE_LOG_* (184-185)
        Передача логов удалённо.

    NAMED_VALUE_* (251-252)
        Пользовательские значения.

    MEMORY_VECT (249)
        Состояние памяти.

    MEMINFO (152)
        Информация о памяти.

    Прочее
    ------
    RADIO / RADIO_STATUS (166, 109)
        Состояние радиоканала.

    WIND / WIND_COV (168, 231)
        Оценка ветра.

    RPM (226)
        Скорость вращения моторов.

    POWER_STATUS (125)
        Питание системы.

    FILE_TRANSFER_PROTOCOL (110)
        Передача файлов.

    TIMESYNC (111)
        Синхронизация времени.

    MESSAGE_INTERVAL (244)
        Настройка частоты сообщений.

    EXTENDED_SYS_STATE (245)
        Расширенное состояние системы (например, landing state).

    VIBRATION (241)
        Вибрации.

    HOME_POSITION (242)
        Домашняя позиция.

    COLLISION (247)
        Обнаружение столкновений.

    Итог
    ----
    Полный список сообщений определяется MAVLink dialect.
    На практике используется только подмножество, необходимое для:
    - состояния
    - управления
    - телеметрии
    """

    return {
        msg_id: msg_cls.msgname
        for msg_id, msg_cls in mavutil.mavlink.mavlink_map.items()
    }